/** Load contents of a file into a canvas
 *
 *  @param evt      Event from a input type="file"
 *  @param canvas   The canvas to load into
 *  @param func     Function to be called when load is complete
 */

function canvas_loader(evt, canvas, fileUrl, func) {

	// Note: This will not work on local file with Chrome browser...

	var ctx = canvas.getContext('2d');


	if(evt !== null) { //evt is set to null on call for locally uploaded QRs
		var bin = base64DecToArr(getBase64Image(fileUrl));
		var theFile = new Blob([bin], {type:"image/png"})
	} else {
		var theFile = fileUrl;
	}//End if evt not null

	var reader = new FileReader();

	reader.onload = ( function(e) {
		var img = new Image();
		img.onload = function() {  
			canvas.width = img.width;
			canvas.height = img.height;
			ctx.drawImage(img,0,0);
			func();
		};
		img.src = e.target.result; 

		function cb(sdpKey) { //seshpack

			//Update the friends key and share and answer the call!
			window.setTimeout(function() {
				key = JSON.parse(sdpKey.value);
				document.getElementById('key').value = key.sessionId;
				// Click the connect button
				console.log("Clicking connect button...");
				document.getElementById('connect').click();
			}, 3000);

		}
		var sdpKey = document.getElementById('qrlogo_text');
		cb(sdpKey); //callback to update friends key with friends sdp key
		});

	// Read in the image file as a data URL.
	reader.readAsDataURL(theFile);
}
