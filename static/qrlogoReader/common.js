/** Escape text to be output as verbatim html
 *  @param text The text to be escaped
 */

function htmlEscape(text) {
	return text.toString().replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;").replace(new RegExp("\n", "g"),"<br>");
	}


